"use client";
import {
  Document,
  Page,
  Text,
  View,
  StyleSheet,
  Image,
} from "@react-pdf/renderer";

// Types
export interface PurchaseOrderProduct {
  businessProductId: string;
  barcode: string | null;
  productName: string | null;
  quantity: number;
  price: number;
}

export interface PurchaseOrderData {
  purchaseOrderId: string;
  supplier: string | null;
  store: string | null;
  status:
    | "DRAFT"
    | "SUBMITTED"
    | "APPROVED"
    | "REJECTED"
    | "RECEIVED"
    | "PARTIAL"
    | "CANCELLED"
    | "CLOSED";
  orderNumber: number;
  dateExpected: string | null;
  autoGenerated: boolean;
  dateCreated: Date;
  createdBy: string;
  products: PurchaseOrderProduct[];
}

export interface CompanyInfo {
  name: string;
  tagline?: string;
  email?: string;
  phone?: string;
  logoUrl?: string;
}

export interface PurchaseOrderPDFProps {
  orderData: PurchaseOrderData;
  companyInfo?: CompanyInfo;
  currency?: string;
  locale?: string;
}

